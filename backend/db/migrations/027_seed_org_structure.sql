INSERT IGNORE INTO departments (name, division_id)
SELECT d.name, dv.id FROM (
  SELECT 'งานธุรการ' AS name, 'กลุ่มงานบริหารทั่วไป' AS division UNION ALL
  SELECT 'งานการเจ้าหน้าที่', 'กลุ่มงานบริหารทั่วไป' UNION ALL
  SELECT 'งานการเงินและบัญชี', 'กลุ่มงานบริหารทั่วไป' UNION ALL
  SELECT 'งานพัสดุ', 'กลุ่มงานบริหารทั่วไป' UNION ALL

  SELECT 'งานยุทธศาสตร์และแผนงาน', 'กลุ่มงานพัฒนายุทธศาสตร์สาธารณสุข' UNION ALL
  SELECT 'งานข้อมูลข่าวสารสุขภาพ', 'กลุ่มงานพัฒนายุทธศาสตร์สาธารณสุข' UNION ALL

  SELECT 'งานอนามัยแม่และเด็ก', 'กลุ่มงานส่งเสริมสุขภาพ' UNION ALL
  SELECT 'งานอนามัยผู้สูงอายุ', 'กลุ่มงานส่งเสริมสุขภาพ' UNION ALL
  SELECT 'งานโภชนาการ', 'กลุ่มงานส่งเสริมสุขภาพ' UNION ALL

  SELECT 'งานควบคุมโรคติดต่อทั่วไป', 'กลุ่มงานควบคุมโรคติดต่อ' UNION ALL
  SELECT 'งานระบาดวิทยา', 'กลุ่มงานควบคุมโรคติดต่อ' UNION ALL

  SELECT 'งานควบคุมโรคไม่ติดต่อเรื้อรัง', 'กลุ่มงานควบคุมโรคไม่ติดต่อ' UNION ALL
  SELECT 'งานสุขภาพจิต', 'กลุ่มงานควบคุมโรคไม่ติดต่อ' UNION ALL

  SELECT 'งานคุ้มครองผู้บริโภค', 'กลุ่มงานคุ้มครองผู้บริโภคและเภสัชสาธารณสุข' UNION ALL
  SELECT 'งานเภสัชกรรม', 'กลุ่มงานคุ้มครองผู้บริโภคและเภสัชสาธารณสุข' UNION ALL

  SELECT 'งานทันตสาธารณสุข', 'กลุ่มงานทันตสาธารณสุข' UNION ALL

  SELECT 'งานอนามัยสิ่งแวดล้อม', 'กลุ่มงานอนามัยสิ่งแวดล้อมและอาชีวอนามัย' UNION ALL
  SELECT 'งานอาชีวอนามัย', 'กลุ่มงานอนามัยสิ่งแวดล้อมและอาชีวอนามัย' UNION ALL

  SELECT 'งานประกันสุขภาพ', 'กลุ่มงานประกันสุขภาพ' UNION ALL

  SELECT 'งานนิติการ', 'กลุ่มงานนิติการ' UNION ALL

  SELECT 'งานการแพทย์แผนไทย', 'กลุ่มงานการแพทย์แผนไทยและการแพทย์ทางเลือก' UNION ALL

  SELECT 'งานการแพทย์ฉุกเฉิน', 'กลุ่มงานบริการการแพทย์ฉุกเฉินและสาธารณภัย' UNION ALL
  SELECT 'งานสาธารณภัย', 'กลุ่มงานบริการการแพทย์ฉุกเฉินและสาธารณภัย'
) d
JOIN divisions dv ON dv.name = d.division;
